package Flappy_Bee;

public class getnen {
	public int nenX, nenY, speedX;
	public getnen(int x, int y) {
		nenX = x;
		nenY = y;
	}
	public int getNenX() {
		return nenX;
	}

	public int getNenY() {
		return nenY;
	}

	public int getSpeedX() {
		return speedX;
	}

	public void setNenX(int nenX) {
		this.nenX = nenX;

	}

	public void setnenY(int nenY) {
		this.nenY = nenY;
	}
	public void setSpeedX(int speedX) {
		this.speedX = speedX;

	}


}
